    public void PrepararBaseDatos(){
        try{
            String controlador="com.mysql.jdbc.Driver";
            Class.forName(controlador).newInstance();
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error al cargar el controlador");
        }
        
        String DBURL="jdbc:mysql://localhost/tpv_facil";
        String usuario="root";
        String password="";
        try{
            conexion=DriverManager.getConnection(DBURL,usuario,password);
        }catch(SQLException ex){
           Logger.getLogger(Ppal.class.getName()).log(Level.SEVERE, null, ex); 
        }
        try
        {
            sentencia=conexion.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_READ_ONLY);
        } catch (SQLException ex)
        {
            Logger.getLogger(Ppal.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void MostrarInventario(){
        String fecha;
        
        try{
            ResultSet r = sentencia.executeQuery("select * from inventario");
            String titulos[] = {"ID","Nombre","Cantidad","Precio Ind."};
            m = new DefaultTableModel(null, titulos);
            
            String fila[] = new String[4];
            while(r.next()){
                fila[0] = r.getString("Id");
                fila[1] = r.getString("Nombre");
                fila[2] = r.getString("Cantidad");
                fila[3] = r.getString("Precio");
                m.addRow(fila);
            }
            tabla.setModel(m);
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Error al extraer los datos de la tabla"+ex);
        }
    }
    
    private void PrepararTabla(){
        String titulos[] = {"Id","Nombre","Cantidad","Precio Ind."};
        m = new DefaultTableModel(null, titulos);
        tabla.setModel(m);
    }

